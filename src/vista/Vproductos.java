    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int filaEditar = jtbproductos.getSelectedRow();
        int numFS = jtbproductos.getSelectedRowCount();

        if (filaEditar >= 0 && numFS == 1) {
            txtclave.setText(String.valueOf(jtbproductos.getValueAt(filaEditar, 0)));
            txtclave.setEditable(false);
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnguardar.setEnabled(false);
            pasarDatosTabla_CajasTexto(jtbproductos);
        } else {
            JOptionPane.showMessageDialog(null, "debes seleccionar solo una fila");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaInicio = jtbproductos.getSelectedRow();
        int numFS = jtbproductos.getSelectedRowCount();
        ArrayList<String> listclave= new ArrayList();
        String clave_producto = "";
        if (filaInicio >= 0) {
            for (int i = 0; i < numFS; i++) {
                clave_producto = String.valueOf(jtbproductos.getValueAt(i + filaInicio, 0));
                listclave.add(clave_producto);
            }
            System.out.println(listclave);

            for (int i = 0; i < numFS; i++) {
                int rptaUsuario = JOptionPane.showConfirmDialog(null, "Â¿Quiere eliminar el registro con clave_producto=" + clave_producto + "?");
                if (rptaUsuario == 0) {
                    productos.eliminarProducto(Integer.parseInt(clave_producto));
                }
            }
            LlenarTabla(jtbproductos);
        } else {
            JOptionPane.showMessageDialog(null, "debes seleccionar solo una fila");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

