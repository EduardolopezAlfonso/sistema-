     public void LlenarTabla(JTable tablaD) {
        DefaultTableModel modeloTabla = new DefaultTableModel();
        tablaD.setModel(modeloTabla);
        modeloTabla.addColumn("clave_producto");
        modeloTabla.addColumn("nombre");
        modeloTabla.addColumn("presentacion");
        modeloTabla.addColumn("precio_compra");
        modeloTabla.addColumn("precio_venta");
        modeloTabla.addColumn("existencias");
        
        TableColumnModel columnModel = jtbproductos.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(30);
        columnModel.getColumn(1).setPreferredWidth(100);
        columnModel.getColumn(2).setPreferredWidth(100);
        columnModel.getColumn(3).setPreferredWidth(100);
        columnModel.getColumn(4).setPreferredWidth(100);
        columnModel.getColumn(5).setPreferredWidth(100);
        

        Object[] columna = new Object[6];
        int numReg = productos.listProductos().size();

        for (int i = 0; i < numReg; i++) {
            columna[0] = productos.listProductos().get(i).getClave_producto();
            columna[1] = productos.listProductos().get(i).getNombre();
            columna[2] = productos.listProductos().get(i).getPresentacion();
            columna[3] = productos.listProductos().get(i).getPrecio_compra();
            columna[4] = productos.listProductos().get(i).getPrecio_venta();
            columna[5] = productos.listProductos().get(i).getExistencias();
            modeloTabla.addRow(columna);
        }
     }
     public void pasarDatosTabla_CajasTexto(JTable tablaD) {
        int fila = tablaD.getSelectedRow();

        txtclave.setText(tablaD.getModel().getValueAt(fila, 0).toString());
        txtnombre.setText(tablaD.getModel().getValueAt(fila, 1).toString());
        txtpresentacion.setText(tablaD.getModel().getValueAt(fila, 2).toString());
        txtcompra.setText(tablaD.getModel().getValueAt(fila, 3).toString());
        txtventa.setText(tablaD.getModel().getValueAt(fila, 4).toString());
        txtexistencias.setText(tablaD.getModel().getValueAt(fila, 5).toString());
        
     }

