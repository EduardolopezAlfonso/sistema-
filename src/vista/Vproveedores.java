    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int filaEditar = jtbproveedores.getSelectedRow();
        int numFS = jtbproveedores.getSelectedRowCount();

        if (filaEditar >= 0 && numFS == 1) {
            txtId.setText(String.valueOf(jtbproveedores.getValueAt(filaEditar, 0)));
            txtId.setEditable(false);
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnguardar.setEnabled(false);
            pasarDatosTabla_CajasTexto(jtbproveedores);
        } else {
            JOptionPane.showMessageDialog(null, "debes seleccionar solo una fila");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

