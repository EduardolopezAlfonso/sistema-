    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaInicio = jtbproveedores.getSelectedRow();
        int numFS = jtbproveedores.getSelectedRowCount();
        ArrayList<String> listId = new ArrayList();
        String id = "";
        if (filaInicio >= 0) {
            for (int i = 0; i < numFS; i++) {
                id = String.valueOf(jtbproveedores.getValueAt(i + filaInicio, 0));
                listId.add(id);
            }
            System.out.println(listId);

            for (int i = 0; i < numFS; i++) {
                int rptaUsuario = JOptionPane.showConfirmDialog(null, "Â¿Quiere eliminar el registro con id=" + id + "?");
                if (rptaUsuario == 0) {
                    proveedores.eliminarProovedor(Integer.parseInt(id));
                }
            }
            LlenarTabla(jtbproveedores);
        } else {
            JOptionPane.showMessageDialog(null, "debes seleccionar solo una fila");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

